    <h4>Cannon</h4>
    <p>
      The Hussites employed two types of cannon, the lighter of the two being
      the most prevalent. The “Tarasnice” was like the hand gonne of the time a
      tube on a stick but was of a larger caliber, perhaps anywhere from .75 to
      1.00. It was mounted on a small platform that initially was not wheeled
      but had wheels added later and was employed between the wagons of the
      wagonberg. The “Houfnice” was the wheeled heavy artillery of the Hussites
      and was employed on special wagons. There is reason to believe that their
      first appearance was at the battle of Aussig in 1426, but wheeled heavy
      artillery was already in use in other parts of Europe even earlier. I can
      only surmise from photos that this artillery piece would fire a projectile
      at least several inches in diameter.
    </p>
    <h4>Cannister or grape shot</h4>
    <p>
      There is evidence to support the use of canister/grapeshot/ buckshot in
      firearms against the Teutonic Knights in 1410 as well as by Hussite
      mercenaries fighting for the Poles in the late 15th century. Given this it
      would be very probable that the Hussites used it as well.
    </p>
    <h4>Polearms</h4>
    <p>
      The Hussites used a variety of polearms, primary the purpose of which were
      to forcibly dismount a rider, making him more vulnerable to dismounted
      attack. The weapon most commonly associated with the Hussites is the
      flail, an agricultural threshing tool with spikes placed on the short end
      chained to a 6 foot pole. Halberds, bardiches, axes and any number of
      polearms found their way into the hands of the Hussites as a result of
      “battlefield pick ups.”
    </p>
