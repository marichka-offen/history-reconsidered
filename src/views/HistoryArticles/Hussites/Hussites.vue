      providing support to the Teutonic Knights. Despite the departure of
      Korybut, it is safe to assume the continued presence of Poles and
      Lithuanians among the Bohemians. Once more in 1433 the Hussites joined the
      Polish-Lithuanian Union in a war against the Teutonic knights and
      penetrated north all the way to the mouth of the Vistula River on the
