    <h3>Hussite Sects</h3>
    <h4>Utraquists</h4>
    <p>
      The name is based on the practice of taking communion “under both kinds”,
      or “sub utraque specie.” This was the moderate element of the Hussites,
      comprised mostly of the merchant class and minor nobility in the urban
      areas, especially Prague. They benefited greatly from the confiscation of
      property from the Catholic churches. This faction attempted to negotiate
      with Emperor Sigismund in 1419-1420 with no success.
    </p>
    <h4>Taborites</h4>
    <p>
      The Taborites represented the most radical sect in Hussitism, believing in
      equality under the law for noble and commoner, the equal distribution of
      wealth, communal ownership of property and decision making by elected
      council rather than noble fiat. To the Taborites, clergy were unnecessary,
      biblical interpretation was an individual matter, their communion services
      were held on hilltops and their children were baptized in rivers and
      ponds. They established themselves in the fortress town of Tabor.
    </p>
    <h4>Orebites</h4>
    <p>
      Established by Ziska in 1423 under the name “Oreb Union” named after a
      hill near the town Orebovice. He had become dissatisfied with the
      indiscipline of the Taborites and introduced a strict military discipline
      among this group he took with him out of the Taborite faction. He enforced
      a strict system of justice under which all were seen as equal under the
      law, all loot was equally distributed and hoarders were executed. Upon the
      death of Ziska, they took the name of “Orphans” and carried a banner with
      the image of Ziska on a white horse.
      <br />
      <br />Adamites. A small short lived sect of Hussites that believed that
      all should live as Adam and Eve did. Their belief in the need to go naked
      doubtless contributed to their suppression by Ziska in 1421.
    </p>
