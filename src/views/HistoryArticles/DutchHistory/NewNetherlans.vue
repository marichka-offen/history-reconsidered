    <p>
      Since the late 16th century the Dutch professional armies were comprised mostly of foreigners seeking to sell their services in the wars with Spain. By virtue of their mercenary status, these foreigners would be more inclined to do the bidding of their paymasters, the House of Orange, and so represented a potential threat to the freedoms of the Dutch people. The professional army was viewed as a necessary evil in 17th century Dutch Society. Not so the Burger Guard and militia. The Dutch view of the homegrown, part time soldier was quite positive and to participate was seen as an adult male’s civic duty and represented an expression of civic responsibility. The prevalent attitude about the two military institutions present in New Netherlands would in all likelihood be similar to the attitudes of the Dutch in the Netherlands.
      <br>
      <br>The variety of equipment in the hands of the military organizations of New Netherlands would have been considerable for a variety of reasons. One of the reasons would have been the requirement for the armed civilians to provide their own longarms, swords and means of carrying powder and shot. The professional military force would have had a variety of equipment as well due to their deployment to the colony over time from a nation with no uniformity of equipment for its own forces. Each soldier deployed to New Netherlands was supplied with longarm, sword and either a bandoleer or cartridge box.
      <br>
      <br>The technological change in firearm ignition systems would have had an impact over time on the types of longarms found in New Netherlands. In all likelihood there would have been a considerable number of matchlocks with some firelocks present. The way ammunition was being carried was transitioning from bandoleers to cartridge boxes on the European continent, so it is likely that both would have been seen in the professional as well as armed civilian military organizations.
      <br>
      <br>There is no evidence that either the armed civilians or professional soldiers in New Netherlands wore uniforms, but there were chain mail coats requested for use in the early 1640’s. Insignia of rank would have been worn with civilian clothes. It is likely that Officers and Sergeants with Officers carrying the Partizan and Sergeants the Halberd as symbols of military rank would have worn the tawny orange sash.
      <br>
      <br>Swords would have been worn by most of the members of a military organization of the time and would have been of a quality appropriate to the social station of the wearer. There is no evidence that pike were present in New Netherlands.
