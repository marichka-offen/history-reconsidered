    <p>
      In New Netherlands there were two distinct military organizations, one comprised of hired professionals and the other of armed citizens.
      <br>
      <br>The army, which was hired by the Dutch West Indies Company (WIC) but paid for by the Dutch Government, consisted of volunteers drawn from Holland as well as New Netherlands. The tour of duty lasted one year, so it was an ideal means for those who wanted to leave Holland to get to New Netherlands and for those already in New Netherlands to have a job for at least a year. The army units in New Netherlands were stationed in small numbers at several different locations throughout the colony (see Table 2. Garrisons.). A civil proprietor employed by the WIC called a “comis,” who was also in charge of the Company traders, would have commanded the Army detachments in the forts. This military organization was likely to have a high percentage of foreigners.
      <br>
      <br>The Burgher Guard, referred to as the schutterei, consisted of all adult males who could afford to pay the fee to become a burgher in a community of sufficient size to support this military organization. The burgher fees were relatively high, 50 Guilders for the greater burgerecht and 20 Guilders for the lesser burgerecht. These high fees prevented a large number of non-Dutch from joining the Burgher Guard. The leadership of the Burgher Guard mirrored the hierarchy of the community with the upper tier of burghers assuming the role of officers i.e., Lieutenants, Ensigns, Cadets and the lesser burghers assuming the role of Sergeant, Corporal and Soldier. The only Burgher Guard in New Netherlands I have found record of was established in New Amsterdam on 9 May 1640. It was organized into two companies, the Orange and Blue Companies, with a third company being added in1658.
      <br>
      <br>The militia was present in the smaller communities and would have likely had a command structure based on the social hierarchy in the community. As there was no Burgher Guard in these smaller communities, there would be no fees to pay and so the militia would be comprised of the entire adult male population.
      Both military organization would have been responsible for security of the settlement by providing a night watch as well as assisting the schout, the equivalent of a sheriff and prosecuting attorney in Dutch communities, in making arrests. The professional soldiers would have assumed responsibility for the security of the fort to which they were assigned and in which they resided.
      <br>
      <br>The ranks structure of the militia, Burgher Guard and army would have likely been the same. I can find no evidence of a rank above Lieutenant in the literature and I think it safe to assume that Lieutenants would have commanded formations that approximated the size of companies (about 100 men). Ensigns would have served as the second in command of the formation and been responsible for the colors. I would assume that cadets were perhaps officers in training. The senior non-commissioned officer of a company-sized formation would have been the Sergeant, with Corporals in charge of files comprised of perhaps five to ten soldiers
