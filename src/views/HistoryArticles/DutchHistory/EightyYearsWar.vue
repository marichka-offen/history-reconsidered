    <h3>Background</h3>
    <p>
      As a result of wars, marriages and political maneuvering most of the
      region currently known as Belgium and The Netherlands were in the hands of
      the Dukes of Burgundy. Charles V the Holy Roman Emperor was the ruler of
      this region by 1515. By the mid 16th century the Hapsburg controlled
      Netherlands was comprised of 3 million people with 300 cities in 17
      provinces, the southern three Walloon speaking and the northern 14
      speaking Dutch. The representative assembly for the Netherlands was the
      States General (“Staten-Generaal”), first called into being by Philip the
      Good in 1464. Antwerp was one of the economic centers of Europe and the
      place from which the gold bullion coming from the New World was exchanged.
      The Hapsburg-Valois Wars with France ended in 1559 with the Treaty of
      Cateau-Cambrésis making the movement of people and ideas easier in Western
      Europe. One of the ides on the move was Calvinist Protestantism out of
      Geneva. It found particular support from the numerous lower class, lesser
      noble and townsfolk in the Netherlands.
    </p>
