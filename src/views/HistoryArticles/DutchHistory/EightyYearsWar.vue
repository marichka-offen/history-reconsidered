    <h4>Origin of the term “beggars” (geuzen)</h4>
    <p>
      On the 5th of April 1566 Louis of Nassau and Count Brederode, both nobles
      sympathetic to the Protestants of the Netherlands, along with 400 of their
      fellow nobles presented the Compromise of Breda to Margaret of Parma, the
      illegitimate sister of King Phillip of Spain and his regent in the
      Netherlands. The Compromise was a request for an end to the work of the
      Inquisition and a removal of Spanish occupying forces for the Netherlands.
      The regent was at first alarmed at the appearance of so large a body, but
      one of her councilors, Berlaymont, was heard to exclaim, “What, madam, is
      your highness afraid of these beggars” (“ces gueux”)? At a meeting held by
      some 300 confederates at the Hotel Culemburg three days later, Brederode
      in a speech declared that if need be they were all ready to become beggars
      in their country’s cause. The French word for beggar is gueux which was
      rapidly transformed into geuzen and became the proud title of those who
      opposed Spanish rule in the Netherlands and “Vivent le geux” was heard on
      the streets of the Netherlands.
    </p>
