    <h3>Recruitment</h3>
    <blockquote>“Whereas the Scottish regiment under the command of George, Lord Douglas, did, in obedience to our orders, come over readily and cheerfully to our service, wee are resolved to intertain and recruite the same although we think fitt to raise the greatest part of the recruits here in this kingdom because of the leavies now making in Scotland for our service there yet , seeing the regiment is a Scottish regiment, wee have thought fitt to raise for it two hundreth men in that our ancient kingdom of Scotland”</blockquote>
    <h6>Letter from Charles II to the Privy Council, September 1666</h6>
    <p>The above quote makes it clear that at least one occasion, and perhaps more, soldiers from England were recruited to serve in the Regiment. There was some bias against service in the Regiment in parts of Scotland and England because it was considered a French Regiment and the officers were, for the most part, Catholics.</p>
    <h3>Company Organization</h3>
    <p>
      At the Time of the Restoration, a Company of Foot was authorized one Captain, one Lieutenant, one Ensign, three Sergeants, three Corporals, 2 Drummers and 100 ”Private Centinells”. Ideally, the company would have been comprised of one third pikemen and two-thirds musketeers.
      <br>
      <br>On 16 July, 1685, James II decreed that the company organizations would change to one Captain, the Lieutenant position to be abolished as vacated, one Ensign, 2 Sergeants, three Corporals, one Drummer and 60 “Private Centinells.” It is doubtful that either company organization was ever at full strength.
    </p>
