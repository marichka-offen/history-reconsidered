    <p>
      Each of the five companies had 200 soldiers assigned including six
      Sergeants, three Corporals and one drummer. The 1st Guards sent 158
      soldiers, the 2nd Guards sent 84 soldiers, the Duke of York’s sent 59
      soldiers, and the Holland Regiment sent 49 soldiers. The non-regimented
      companies at Portsmouth, Plymouth, Tower of London, Gravesend, Tilbury,
      Hull, Sheerness and Windsor provided a total of 150 soldiers. A total of
      500 men were newly raised and kept in the Tower of London until
      embarkation to prevent desertion. The companies were armed with the then
      typical ratio of 2 to 1 of musket to pike, with the possible exception of
      the Duke of York’s Regiment which was a musket only equipped regiment. The
      First Guards companies wore red coats with blue facings, The Second Guards
      company wore red coats with green facings, the Admirals Regiment company
      wore gold coats with red facings and the Holland Regiment company wore red
      coats with “flesh” colored facings. Unusually, it is very likely that the
      companies wore the uniforms of their parent regiments. Equally unusually,
      the colors carried by the regiment reflected the origins of the companies
      in the regiment:
    </p>
    <blockquote>
      "Nov 1676 “Warrant to Ralph Montague, Master of the King's Great Wardrobe.
      To prepare two colours for each of the five companies of the King's foot
      guards, now sending upon an expedition to Virginia, viz., for Captain
      Herbert Jeffreys' company, two colours, with a crowned lion passant upon
      the crown; for Captain Edward Picks' company two colours, with the Royal
      oak crowned; for Captain John Mutlowe's company two colours, the ground
      blue with a red cross in a white field; for Captain Charles Middleton's
      company two colours, white waved with lemon, equally mixed with the red
      cross quite through with J. D. Y. in cipher, in gold; and for Captain
      William Meole's company two colours, the ground green with a red cross in
      a white field.”
    </blockquote>
    <h6>
      Calendar of State Papers Colonial, America and West Indies,Vol. 9, 1675-76
    </h6>
    <p>
      The regiment carried additional equipment including 700 muskets, 300
      pikes, 4 small field pieces 3 pounders, 2 small mortar pieces, 1,000 hand
      grenades, the musket and pike destined to arm the militia, in all
      probability, as opposed to regimental spare equipment. The Regiment also
      took 1,500 “swinefeathers” a by late 17th century archaic item consisting
      of a 6 to 7 foot pikestaff with steel points at both ends and a hook to
      support a musket or pole approximately half way down the length of the
      shaft. It is probable that the swinefeathers were taken to serve as field
      expedient “cheviot de frieze” engineer obstacles. The swinefeathers were
      included in an inventory of “His Majesties Magazine at the Middle
      Plantation” in 1678 and it is doubtful if they ever saw any use. Doubtless
      more useful, were the 300 hoes and cloth bags taken to Virginia. In order
      to support the Regiment on the 6th of November, 1676 a Warrant to the
      Master General of the Ordnance ordered them:
    </p>
    <blockquote>
      “To deliver out of his Majesty's stores fire hundred arms more for the use
      of forces now sending upon an expedition to Virginia, also to prepare
      estimate for fifteen drums, halberds, powder, shot, lanterns, and candles,
      in a fit proportion to the one thousand men now going upon this
      expedition; and, lastly, for five tents of double canvas for the five
      captains commanding said forces.”
    </blockquote>
    <h6>
      Calendar of State Papers Colonial, America and West Indies, Vol. 9,
      1675-76
    </h6>
    <p>
      Before boarding their vessels for a mid winter Atlantic crossing, the
      troops were issued biscuits and cheese sufficient for three months. On the
      24th of November, 1676 the expedition sailed distributed across the fleet
      as follows:
    </p>
    <blockquote>
      “In the Henry and Anne, Captain Jeffreys, Lieutenant Rouse, and Ensign
      Grymes with 195 men. In the Constant a Sergeant and 19 men. In the
      Barnaby, Captain Edward Picks, Lieutenant Hetley and Ensign William
      Mathews, and 116 men. In the Recovery, Lieutenant Webb, the rest of
      Captain Picks' officers and men to make up 100. In the William and Robert,
      Captain Charles Middleton, Lieutenants Francis Hobbin and Thorne, and 167
      men. In the Constant, Ensign John Jeffreys and 48 men. In the Francis,
      Captain John Mutlowe, Lieutenant Thomas Troutbeck, Ensign Seymour and 97
      men. In the Merchant's Consent, Lieutenant John Tonge and 79 men, and 39
      men in the Constant. In the Golden Lyon, Captain William Meoles,
      Lieutenant Thomas Sanders and 148 men. In the Constant, Lieutenant Walter
      Morgan and the rest of the officers to make up 69 men. Total, 1,094
      officers and men on board eight ships.”
    </blockquote>
    <h6>
      Calendar of State Papers Colonial, America and West Indies, Vol 9, 1675-76
    </h6>
    <p>
      The first ship arriving at the ruins of Jamestown was the Bristol ( not
      mentioned in the above extract, along with Dartmouth, Rose and Deptford)
      on 29 January 1677. On board was Sir John Berry, Commissioner and
      Commander of the Naval expedition, Captain Morris and seventy soldiers of
      Jeffreys’ Regiment. The rest of the fleet arrived by 5 February. Some
      insight as to the condition of the troops after the ten week crossing can
      be gained from a report from the Commissioners to the King in June of 1677
      stating that “In Captain Middleton’s Company there was about 150 such men
      and officers all sick and that the like number of sick were in every other
      company.” The supplies issued to the troops for the crossing were
      completely depleted as well. The Regiment was forced to rely on colonists
      for food and billeting who had barely enough to take care of themselves in
      the wake of Bacon’s Rebellion. By February, the Regiment was headquartered
      at the Middle Plantation, the future site of Williamsburg, with Captain
      Meoles’ Company and another sent to Falls of the James River, the future
      site of Richmond, one company was sent to Nansemond County, one remained
      at the Middle Plantation and one was sent to New Kent County. Those
      stationed at the outlying locations were recalled to the Middle Plantation
      in late May.
      <br />
      <br />In May 1677, Colonel Jeffreys, now made the Lieutenant Governor to
      replace Lord Berkeley, was ordered to return to England leaving behind one
      hundred men and any of the soldiers who wished to stay as colonists.
      Elements of the Regiment returning to England were not to depart for
      another seven months. Those soldiers that remained in service from the 2nd
      Guards, Holland Regiment and Admirals Regiment changed their coats for the
      red with blue facings of the First Guards.
      <br />
      <br />The Commissioners, as part of their mission to Virginia, established
      peace with the local tribes and chose the Middle Plantation as the
      location for the ceremonial signing which took place on the King’s
      Birthday, 29 May 1677. The Regiment was paraded for this event at which,
      with their followers, “were present the Queen of the Pamunkey, her son,
      Captain John West, The Queen of Weyanoke, the King of the Nottoways, and
      King of the Nansemonds.”
      <br />
      <br />Part of the Regiment under the command of Lieutenant Tongue, was
      sent to Isle of Wight and Nansemond from mid July 1677 to January 1678.
      During the same time, part of the regiment was in James and York Counties.
      <br />
      <br />On 10 January 1678 the Unity took part of the Regiment back to
      England, arriving at Gravesend on 23 March with Lieutenant Rous with 76
      soldiers from Colonel Jeffreys’ Company, Captain Mutlowe with Lieutenant
      Troutbeck and 67 soldiers, and Captain Picts with Ensign Saville and 74
      soldiers. This represented a total of 5 Officers and 215 Men. Colonel
      Jeffreys remained in Virginia as did the Regimental Adjutant, Captain
      William Morris and the Quartermaster, Lieutenant Tongue. James Archer, the
      Engineer, remained in service of the Company as late its disbandment in
      1682 and appears in the record as late as 1690 as a Captain in the
      Virginia Militia. The Chaplain and Churgeon were discharged in 1679. The
      literature makes it clear that Captain Meoles was deceased by January of
      1678, based on a request from Lieutenant Tonge for his command. The
      officers not present in the Unity and that are unaccounted for in the
      literature are Lieutenant Henry Taylor, Ensign William Matthews, Ensign
      Thomas Seymour, Lieutenant John Webb, Second Lieutenant John Hetley (or
      Thomas Moile), Captain Charles Middleton, Lieutenant Francis
      Hobbin/Hellin, Second Lieutenant John Thorne, Ensign John Jeffreys,
      Lieutenant Walter Morgan, Second Lieutenant Thomas Sanders, Ensign George
      Agne, Ensign Grimes, Captain Ralph Delaval and Ensign Francis Wheeler for
      a total of 16 Officers.
      <br />
      <br />A second group of soldiers under command of Lieutenant Collier of
      Picks’ Company arrived in England in April/May of 1678. An entry in the
      “Calendar of States Papers Colonial, America and West Indies” states that
      there were a total of 375 soldiers returned to England from Virginia out
      of Jeffreys’ Regiment. If that figure is correct, and we exclude the 100
      men of Jeffreys Company, made up of 20 men from each company who remained
      in Virginia, we have 525 soldiers who either died in service or chose to
      stay in Virginia. It is interesting to note that the Unity returned with
      soldiers from the 1st and 2nd Guards and that Lieutenant Collier was in a
      company from 2nd Guards as well. Could it be that those who chose to stay
      in Virginia as civilians were predominantly from the Duke of York’s
      Regiment and the Holland Regiment?
      <br />
      <br />In November of 1678, Jeffreys led his company to the Falls of the
      James in response to an Indian attack on the frontier, arriving too late
      to make a difference. A party of thirteen Oneida were attacked by eighteen
      mounted Virginians and the Oneida killed two horsemen and their horses.
      The Oneida then attacked a house, taking two women and four children
      captive. The captives were returned to Virginia agents in early 1679 as a
      result of the intercession of the then Governor of New York, Sir Edmund
      Andros, who had a treaty with the Oneida.
      <br />
      <br />Plans for a second company in Virginia began in August of 1678, two
      months after Lieutenant Collier and his men returned to England, with the
      granting of a commission to Thomas, Lord Culpepper, Governor of Virginia
      as Captain of a Company of Foot along with Thomas Leigh, Lieutenant and
      William Armiger, Ensign. It would not be until September 1679 that the
      Company would embark for Virginia.
      <br />
      <br />In December of 1678 Colonel Herbert Jeffreys’ died and was replaced
      by Sir Henry Chicheley as Lieutenant Governor and Captain of Jeffreys’
      Company. The Colony of Virginia established at their own expense, in April
      1679, four garrison and store houses that were to be constructed at the
      head of the Rappahannock, Potomac, James and Mattaponi Rivers and to be
      garrisoned by militia. In May of 1680, the Virginia Assembly asked that
      ten soldiers from Chicheley’s and or Culpepper’s Companies replace an
      equal number of militia at each of the four garrison houses to relieve the
      burden on the militia. Of those forty soldiers, we know from the record
      that thirty eight of them were described as mutinous likely due to the
      lack of pay. By 1681 Chicheley’s Company, or elements of it, was paid for
      musters as opposed to continuous service, having served seven musters of
      fourteen months from May 1678 to July 1680.
      <br />
      <br />Captain Morris, the adjutant to Jeffreys’ Regiment, was still in
      service in the Virginia Companies as late as 1681 reporting to the Lords
      of Trade and Plantations in October of that year that both companies were
      four soldiers short of being at full strength and that the extreme poverty
      of the colony was a source of potential unrest. His observations proved to
      be prescient in that there was a rash of tobacco cutting in May of 1682.
      <br />
      <br />The cutting of tobacco plants while still in the fields occurred in
      response to low tobacco prices and seems to have been a hysterical effort
      to raise the value of the remaining tobacco. This civil disturbance
      occurred at the same time as the disbandment of Chicheley’s and
      Culpepper’s Companies. The soldiers were unsatisfied with the terms of
      disbandment and so only contributed to the problem they could have helped
      quell, if still in service with steady pay. As the Secretary of Virginia
      wrote to Sir Leoline Jenkins, “They are therefore so far from being as
      assistance at the only moment they are wanted since their arrival, that
      their mutinous temper doubles our apprehension of evil events.” The
      disbandment was complete by June 1682 with, “Their arms, partisans,
      halberts and drums are returned into store and care has been taken for the
      passages of such as wished to return to England. Many more are provided
      for by being entertained by the garrisons at the heads of the rivers.”
      Eight of the soldiers were retained to serve as the Governors Guard and it
      is uncertain how long they remained in service.
    </p>
    <h3>Conclusions</h3>
    <p>
      Given the absence of military action, it is difficult to assess the
      contribution of Jeffreys Regiment and the Companies formed from it. Their
      contribution could have been preemptive, that is to say, perhaps their
      presence in Virginia prevented colonial unrest and Indian incursions
      rather than stopping them in process. It is equally difficult to assess
      the effect of those former Regular Officers who remained in the Virginia
      colony but there is a reoccurrence of a few of their names in positions of
      responsibility in colonial service so they continued to have some
      influence on events.
      <br />
      <br />The Regiment was unique in terms of retaining unit of origin colours
      and uniforms and the speed with which the unit was formed and deployed.
      The regiment and follow on companies also represent one of the first
      examples of a trend in sending British Regulars to colonies in the
      Caribbean and North America in order to assert control over colonies that
      in most cases had been for the most part ignored or left alone by the
      Crown.
      <br />
      <br />The record of Jeffreys Regiment and the companies that followed it
      is frustratingly incomplete. You will find large gaps in time for which we
      found no information about the units and their activities. There also is a
      real lack of information about the soldiers and officers who served. Any
      insights or sources that would help make a more complete record would be
      greatly appreciated
    </p>
