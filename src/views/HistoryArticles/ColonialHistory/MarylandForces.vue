    <h6>Maryland Gazette, 29 April 1756</h6>
    <blockquote>
      “We mentioned in our last that Mr. Thomas Cresap, junr., and his brother
      with a party of men painted and dress’d like Indians, were gone out in
      pursuit of the enemy: since which we are now informed by Capt. Dagworthy
      (who came to town yesterday from Fort Cumberland), that on the 23d of
      April, as Mr. Cresap and his party lay in ambush near the Little-Meadows,
      they saw a party of Indians coming by them; but one of the party firing
      too soon, alarm’d them, and they fled as fast as possible into thickets,
      leaving their horses and baggage, which out people took and brought off
      with them; among their baggage one scalp was found. One of the Indians
      taking a different course from the rest; Mr. Cresap, and two others after
      him near a mile, when the Indians finding that Mr. Cresap gain’d on him
      and would overtake him, he dodged behind a large tree and Mr. Cresap
      stopp’d behind one smaller; and they fired at one another so near together
      that it could not be distinguish’d which fired first. Mr. Cresap was shot
      with large shot in the breast, and the others of his party coming up, he
      told them, not to mind him, he was a dead man, but to pursue the enemy;
      and then dropped down dead. The Indian was shot thro’ the right breast ,
      but was not dead when they came up to him, and so they dispatch’d him with
      a tomahawk and scalp’d him. Mr. Cresap’s body they buried as privately as
      they could. He was a young widower, and has left two little children and
      his death is lamented by all who knew him.”
    </blockquote>
