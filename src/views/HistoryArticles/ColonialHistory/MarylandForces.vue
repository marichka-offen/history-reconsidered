    <blockquote>
      “Sir: When I was down, the Committee among other things resolved, that the
      Maryland and Carolina Companies should not be supported with our
      Provisions. This resolve (I think) met with your approbation; upon which I
      wrote to Colonel Stephen, desiring him to acquaint Captain Dagworthy
      thereof, who paid slight regard to it, saying it was in the King's
      garison, and all the troops had an equal right to draw provisions with us,
      by his order, (as commanding officer,) and that we, after it was put
      there, had no Power to remove it without his leave. I should, therefore,
      be glad not care to act without instructions, lest it should appear to
      proceed from pique and resentment at having the command disputed. This is
      one among the numberless Inconveniences of having the Fort in Maryland.
      Captain Dagworthy, I dare venture to affirm, is encouraged to say this by
      Governor Sharpe, who we know has wrote to him to keep the command. This
      Captain Dagworthy acquainted Colonel Stephen of himself. As I have not yet
      heard how General Shirley has answered your Honor's request, I fear the
      success, especially as it is next to an impossibility (as Governor Sharpe
      has been there to plead Captain Dagworthy's cause) by writing to make the
      General acquainted with the Nature of the Dispute. The Officers have drawn
      up a memorial to be presented to the General, and, that it may be properly
      strengthened, they humbly beg your solicitation to have us (as we have
      certain advices that it is in his power) put upon the Establishment. This
      would at once put an End to Contention, which is the Root of Evil, and
      destructive to the best of Operations; and turn all our Movements into a
      free, easy Channel.”
    </blockquote>
