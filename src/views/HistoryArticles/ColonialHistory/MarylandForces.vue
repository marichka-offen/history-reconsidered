    <h6>Maryland Gazette, 23 June 1757</h6>
    <blockquote>
      “In the letter which I addressed to you on the 26th of May I informed you
      that the Assembly of this Province had in compliance with the Earl of
      Loudoun’s requisition agreed to support five hundred men for the immediate
      defense of the Province and to act on any emergency in conjunction with
      such other forces as should be employed in these parts to annoy His
      Majesty’s enemies. Some of these men were by his Lordship’s Order marched
      to garrison Fort Cumberland while the rest were left at Fort Frederick to
      patrol on our frontiers at a small distance beyond the settlements, and by
      this disposition of them the inhabitants have been so effectually secured
      this summer from incursions of the enemy that we have not lost a single
      person, exclusive of soldiers and such as attend them. The money which had
      been appropriated for the support of these troops being now expended , I
      have convened the Assembly again and recommended it to them to grant
      further supplies for their support, but I am sorry to inform you that they
      have absolutely refused to provide for any men that rare or may be ordered
      to Fort Cumberland , and that they are also resolved to reduce the troops
      in the pay of this Province to three hundred, who shall they say on no
      account whatever be employed otherwise that in garrisoning Fort Frederick
      and patrolling just beyond our frontier settlements.”
    </blockquote>
