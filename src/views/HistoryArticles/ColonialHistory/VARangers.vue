    <blockquote>
      “Whereas it is judged necessary in this time of open war, that a number of
      forces should be raised and kept on foot, for vindicating the honor of his
      majesty's crown, and for the safety and defence of this dominion,
      amounting to fifteen hundred and seventy two men, including three
      companies of rangers, to consist of one hundred men each. ……An exact
      discipline be observed, and that the soldiers who shall mutiny or stir up
      sedition, or shall desert his majesty's service be brought to a more
      exemplary and speedy punishment, than the usual forms of the law will
      allow……. That from and after the passing of this act, if any person being
      mustered, or in pay as an officer, or who is or shall be enlisted or in
      pay as a soldier or ranger, by virtue of any act of Assembly, or shall
      during the continuance of this act voluntarily enter himself into his
      majesty's service as a soldier or ranger, shall at any time during such
      continuance of this act within this dominion, begin, excite, cause, or
      join in any mutiny or sedition in the company or regiment whereto he doth
      belong, or in any other company, or desert his majesty's service, or being
      a soldier or ranger actually enlisted in any company, shall enlist himself
      in any other company, without a discharge produced in writing from the
      colonel, or in his absence the chief officer commanding the regiment or
      company in which he last served as an enlisted soldier or ranger; or if
      any officer, soldier or ranger, so enlisted as aforesaid, shall hold
      correspondence with any of his majesty's enemies, or give them advice or
      intelligence either by letters, messages, signs, or tokens, or any manner
      of way whatsoever, or shall treat with such enemies, or enter into any
      condition with them, without the license of his majesty's
      lieutenant-governor, or commander in chief of this dominion, or the
      colonel or chief officer commanding such regiment; or shall strike or use
      any violence against his superior officer, being in the execution of his
      office, or shall refuse to obey any lawful command of his superior
      officer, all and every person and persons so offending, in any of the
      matters before mentioned, shall suffer death, or such other punishment, as
      by a court martial shall be inflicted That the governor or commander in
      chief of this dominion, may from time to time grant a commission under the
      seal of this colony to any officer of such regiment, not under the degree
      of a field officer, for holding a general court martial within this
      dominion, for the trial of any officer or soldier belonging to the
      Virginia regiment, in which court martial all the offences above
      mentioned, and all other offences herein after specified shall be tried
      and proceeded against in such manner, as by this act shall be hereafter
      directed.”
