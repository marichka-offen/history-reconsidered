    <p>
      <b>18 February - 13 March 1756.</b> After her return from captivity, in
      November of 1755, Mary Ingles provided her husband, CPT William Ingles,
      with intelligence about Shawnee villages along the Ohio. In December of
      1755, CPT Ingles approached GOV Dinwiddie about conducting a raid against
      two of these villages near present day Portsmouth, OH. MAJ Andrew Lewis
      was designated to lead the expedition of approximately 200-300 men from
      the Virginia Regiment and Militia Rangers along with approximately 80-130
      Cherokees. Captain Hogg was ordered to provide and lead forty men from his
      Company. A draft of sixty men from Captain William Preston’s and Captain
      John Smith’s Company, to be commanded by Captain Smith was ordered to
      participate. Captain Overton and Captain Obadiah Woodson were to provide
      forty men each and serve as Company Commanders. Captain Pearis commanded
      the Cherokee’s. Captain Robert Breckinridge, was ordered to take his
      companies as well. Captain’s Archibald Alexander, John Montgomery and
      Dunlap commanded volunteer companies of indeterminate size. Captain David
      Stuart served as commissary. CPT William Ingles, the instigator if not,
      perhaps, the planner of the mission, joined the expedition as well. The
      main body of the expedition departed Fort Frederick (vicinity Salem, VA)
      on 18 February 1756. The expedition with the exception of CPT Hogg’s men
      who were behind the main body, reached the north Fork of the Holston River
      by 23 February. By 26 February the expedition was at the head of the
      Clinch River and at the head of Sandy Creek by the 28th which they
      followed towards the Ohio River until desertion and insubordination caused
      the expedition to turn around on 13 March.
