    <h2>The Virginia Ranger Companies, 1755- 1763</h2>
    <p>
      During this period, the western settlements of the colony of Virginia were
      subject to Native American raids, initially as part of the French and
      Indian War, then Pontiac Rebellion and finally war with the Cherokees. The
      colony of Virginia raised companies of Rangers to assist in the defense of
      its vast western territories. The Virginia Regiment and Militia were the
      other military organizations participating in the defense of western
      Virginia.
      <br>
      <br>The Ranger Companies were raised on an annual basis and subject to
      disbandment or continuation, situation and budget dependent. They were
      typically raised in specific counties for service in that county. There
      are at least two exceptions to this, however, the expedition to the Ohio
      River and the expedition to the Cherokees, both in 1756 and both conducted
      in whole or part with Ranger Companies. Raising men for local service
      meant that they probably had some familiarity with settlements, forts and
      terrain in their area of operations. Their degree of familiarity would
      have been far greater than that of militia sent into western counties for
      brief periods of service and the Virginia Regiment which was raised from
      all the counties of Virginia.
      <br>
      <br>The men were paid a daily rate with arms provided by the colony, if
      required. There was clothing provided to at least two of the Companies in
      1755, with consequent stoppages or deductions in pay. Clothing was
      apparently provided by Lord Fairfax on one occasion in 1756 as well. There
      is no reason to believe the clothing provided was the blue uniform with
      red facings worn by the Virginia Regiment. There is no record of any
      attempt on the part of the Colony of Virginia or of the Ranger Company
      Commanders to provide any sort of distinctive uniform or insignia to the
      Ranger Companies. There was an active market for used clothing in Britain
      and her Colonies in the 18th century and that is the likely sources for
      clothing provided to the Ranger Companies in 1755. After 1755, it was
      specifically stated that the men were responsible for providing their own
      clothing. This being the case, men of the Ranger Companies would have worn
      the civilian clothing of the period.
      <br>
      <br>The planned strength for the Ranger Companies was 100. Actual
      Company strength, based on existing accounts, ranged from as many as 60
      men to as few as 10. There is no way of knowing the company strengths when
      raised to see if any of them actually enlisted 100 men and no way to
      determine losses over time to illness, death and desertion. All we are
      left with is numerical snap shots and the occasional disparaging remarks
      from Governor Dinwiddie and Colonel Washington about absence and
      desertion.
      <br>
      <br>There was a failed attempt to incorporate the Ranger Companies into
      the Virginia Regiment in 1756. It seems the terms of service, and very
      likely the discipline, were the reason the Ranger Companies declined the
      request. It was not until 1757 and the ‘Act for Preventing Mutiny and
      Desertion’ that the Ranger Companies and the Virginia Regiment were
      subject to the same military law. This did not seem to prevent the raising
      of Ranger Companies and there is no way of knowing if it had an effect on
      recruitment for those companies raised.
      <br>
      <br>Like all military organizations of the 18th century, women had a
      critical logistical role. Women would have been present on forts,
      blockhouses and settlements across western Virginia. The women in those
      settlements would have repaired and altered clothing as necessary, cooked
      meals and provided medical care to the sick and wounded. They were even
      known to have participated in the defense of their homes. There would have
      no doubt been children present as well.
    </p>
