    <i>
      <h4>The Boyne Water</h4>
      <br />July the first, of a morning clear, one thousand six hundred and
      ninety, <br />King William did his men prepare—of thousands he had thirty-
      <br />To fight King James and all his foes, encamped near the Boyne Water;
      <br />He little feared, though two to one, their multitude to scatter.
      <br />
      <br />King William called his officers, saying: "Gentlemen, mind your
      station, <br />And let your valour here be shown before this Irish nation;
      <br />My brazen walls let no man break, and your subtle foes you’ll
      scatter, <br />Be sure you show them good English play as you go over the
      water."
      <br />
      <br />Both foot and horse they marched on, intending them to batter,
      <br />But the brave Duke Schomberg he was shot as he crossed over the
      water. <br />When that King William did observe the brave Duke Schomberg
      falling, <br />He reined his horse with a heavy heart, on the
      Enniskillenes calling:
      <br />
      <br />"What will you do for me, brave boys—see yonder men retreating?
      <br />Our enemies encouraged are, and English drums are beating." <br />He
      says, "My boys feel no dismay at the losing of one commander, <br />For
      God shall be our King this day, and I'll be general under."
      <br />
      <br />Within four yards of our fore-front, before a shot was fired,
      <br />A sudden snuff they got that day, which little they desired;
      <br />For horse and man fell to the ground, and some hung on their saddle:
      <br />Others turned up their forked ends, which we call coup de ladle.
      <br />
      <br />Prince Eugene's regiment was the next, on our right hand advanced
      <br />Into a field of standing wheat, where Irish horses pranced;
      <br />But the brandy ran so in their heads, their senses all did scatter,
      <br />They little thought to leave their bones that day at the Boyne
      Water.
      <br />
      <br />Both men and horse lay on the ground, and many there lay bleeding,
      <br />I saw no sickles there that day—but, sure, there was sharp shearing.
      <br />Now, praise God, all true Protestants, and heaven's and earth's
      Creator, <br />For the deliverance he sent our enemies to scatter.
      <br />The Church's foes will pine away, like churlish-hearted Nabal,
      <br />For our deliverer came this day like the great Zorobabal.
      <br />
      <br />So praise God, all true Protestants, and I will say no further,
      <br />But had the Papists gained that day, there would have been open
      murder. <br />Although King James and many more were ne'er that way
      inclined, <br />It was not in their power to stop what the rabble they
      designed.
    </i>
