      <h4>Lillibulero</h4>

      <br />Ho! brother Teague, dost hear the decree? <br />Lillibulero bullen a
      la, (repeated after each line) <br />Dat we shall have a new deputy?
      <br />Lillibulero bullen a la.
      <br />
      <br />Refrain: <br />Lero, lero, lillibulero, <br />lillibulero, bullen a
      la <br />Lero, lero, lillibulero, <br />lillibulero, bullen a la.
      <br />
      <br />Ho! by my soul, it is de Talbot, <br />And he will cut all de
      English throat.
      <br />
      <br />Though, by my soul, de English do praat, <br />De law's on dare
      side, and Creish knows what.
      <br />
      <br />But if dispence do come from de Pope, <br />We'll hang Magna Charta
      and dem in a rope.
      <br />
      <br />And de good Talbot is made a Lord, <br />And with brave lads is
      coming abroad.
      <br />
      <br />Who all in France have taken a sware, <br />Dat dey will have no
      Protestant heir.
      <br />
      <br />Ara! but why does he stay behind? <br />Ho' by my soul, 'tis a
      Protestant wind!
      <br />
      <br />But see, de Tyrconnel is now come ashore, <br />And we shall have
      commissions galore.
      <br />
      <br />And he dat will not go to mass <br />Shall be turn out, and look
      like an ass.
      <br />
      <br />But now de hereticks all go down, <br />By Creish and St. Patrick,
      de nation's our own.
      <br />
      <br />Dare was an old prophecy found in a bog, <br />" Ireland shall be
      ruled by an ass and a dog".
      <br />
      <br />And now dis prophecy is come to pass, <br />For Talbot's de dog, and
      James is de ass.
